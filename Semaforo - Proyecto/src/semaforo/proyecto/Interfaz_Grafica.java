    Thread hilo = new Thread(){
        public void run() {
            while(true){
                contador = contador +1 ;
                if(contador>=1 && contador<=60){
                    temp = temp - 1;
                    texto.setText(""+temp);
                }
                if(contador==1){
                    texto1.setText("Peatones pueden caminar");
                    texto1.setForeground(Color.green);
                    verde.setVisible(true);
                    rojo.setVisible(false);
                    amarillo.setVisible(false);
                    nada.setVisible(false); 
                }
                
                if(contador>=60 && contador<=63){
                    temp2 = temp2 - 1;
                    texto.setText(""+temp2);
                }
                if(contador==60){
                    texto1.setText("Reduzca la velocidad");
                    texto1.setForeground(Color.yellow);
                    verde.setVisible(false);
                    amarillo.setVisible(true);
                    rojo.setVisible(false);
                    nada.setVisible(false);
                }
                
                if(contador>=63 && contador<=93){
                    temp3 = temp3 - 1;
                    texto.setText(""+temp3);
                }
                if(contador==63){
                    texto1.setText("Alto");
                    texto1.setForeground(Color.red);
                    verde.setVisible(false);
                    rojo.setVisible(true);
                    amarillo.setVisible(false);
                    nada.setVisible(false);
                }

                if(contador==93){
                    contador=0;
                    temp=59;
                    temp2=5;
                    temp3=59;
                    
                }
                
                try {
                    
                    hilo.sleep(140);
                } catch (InterruptedException ex) {
                    Logger.getLogger(Interfaz_Grafica.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            
        }
        
            
            };

